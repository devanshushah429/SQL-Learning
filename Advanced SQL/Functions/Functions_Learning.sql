--DateTime Functions

-- DATEDIFF
SELECT DATEDIFF(DAY, '2002-02-20', '2002-02-28')
SELECT DATEDIFF(MONTH, '2002-02-20', '2003-02-28')
SELECT DATEDIFF(YEAR, '2002-02-20', '2003-02-28')

-- DATEFROMPARTS
SELECT DATEFROMPARTS(2018, 10, 31) AS DateFromParts

-- DATEADD
SELECT DATEADD(DAY, 4, '2002-02-20')
SELECT DATEADD(MONTH, 4, '2002-02-20')
SELECT DATEADD(YEAR, 4, '2002-02-20')

-- DatePart
SELECT DATEPART(DAY,'2003-04-29')
SELECT DATEPART(MONTH,'2003-04-29')
SELECT DATEPART(YEAR,'2003-04-29')

--ISDATE
SELECT ISDATE('2004-02-29')
-- Conversion Functions

-- CONVERT

-- to varchar
SELECT CONVERT(varchar, 25.65) AS 'string'

-- to datetime
SELECT CONVERT(datetime, '2017-08-25')

-- format 101
SELECT CONVERT(varchar, '2017-08-25', 101)


--CAST

-- to date time
SELECT CAST('2017-08-25' AS datetime)

-- To VARCHAR
SELECT CAST(25.65 AS varchar)

--Parse
SELECT PARSE('25/08/2017' AS DATE USING 'en-GB') AS ParsedDate